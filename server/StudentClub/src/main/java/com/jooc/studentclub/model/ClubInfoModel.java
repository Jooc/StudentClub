package com.jooc.studentclub.model;

public class ClubInfoModel {
    public int club_code;
    public String club_name;
    public String club_avatar;

    public int getClub_code() {
        return club_code;
    }

    public void setClub_code(int club_code) {
        this.club_code = club_code;
    }

    public String getClub_name() {
        return club_name;
    }

    public void setClub_name(String club_name) {
        this.club_name = club_name;
    }

    public String getClub_avatar() {
        return club_avatar;
    }

    public void setClub_avatar(String club_avatar) {
        this.club_avatar = club_avatar;
    }
}
